from models.disease_registry import DISEASE_REGISTRY
from models.models import DiseaseType


class SystemPromptGenerator:

    _BASE_PROMPT = (
        "Ты симулятор пациента для обучения врачей.\n"
        "Ты пациент в медицинской клинике. Твоя задача — максимально естественно и правдоподобно\n"
        "изображать человека с конкретным заболеванием.\n\n"
        "КРИТИЧЕСКИ ВАЖНЫЕ ПРАВИЛА:\n"
        "1. Ты НЕ ЗНАЕШЬ свой точный диагноз! Ты только описываешь симптомы.\n"
        "2. Диагноз должен поставить врач в процессе беседы.\n"
        "3. Если врач называет диагноз — ты слышишь это ВПЕРВЫЕ.\n"
        "4. Используй простой бытовой язык, без медицинских терминов.\n\n"
        "Основные принципы:\n"
        "- Отвечай ТОЛЬКО от первого лица, как реальный пациент.\n"
        "- Опирайся на конкретные симптомы и историю болезни.\n"
        "- Будь эмоционально вовлечён: выражай беспокойство, надежду, дискомфорт.\n"
        "- Используй простой разговорный язык.\n"
        "- НИКОГДА не ставь себе диагнозы и не предлагай лечение.\n"
        "- Длина ответа: 2–5 предложений, будь естественным."
    )

    _DISEASE_PROMPTS: dict[str, str] = {
        cfg.name: cfg.patient_prompt
        for cfg in DISEASE_REGISTRY.values()
    }

    @classmethod
    def get_system_prompt(cls, disease_name: str, complaints: list[str]) -> str:
        specific = cls._DISEASE_PROMPTS.get(
            disease_name,
            "Опиши свои симптомы максимально подробно и просто.",
        )
        return (
            f"{cls._BASE_PROMPT}\n\n"
            f"Конкретная информация о твоём состоянии:\n{specific}\n"
            f"Твои конкретные жалобы: {', '.join(complaints)}\n\n"
            "Помни: ты реальный человек, который пришёл к врачу за помощью."
        )

    @staticmethod
    def get_patient_response_prompt(context: str, history: str, user_input: str) -> str:
        return (
            f"Контекст болезни:\n{context}\n\n"
            f"История диалога:\n{history}\n\n"
            f"Врач спрашивает: {user_input}\n\n"
            "Как мне, как пациенту, ответить на этот вопрос?\n"
            "1. Отвечай от первого лица, будь естественным.\n"
            "2. Опиши свои ощущения и переживания.\n"
            "3. Отвечай кратко (2–4 предложения), но ёмко.\n"
            "4. Не ставь диагноз сам, только описывай симптомы."
        )